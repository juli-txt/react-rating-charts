/**
 * The `Margins` type.
 *
 * This type represents the margins of a component.
 *
 * @prop marginTop - The top margin.
 * @prop marginRight - The right margin.
 * @prop marginBottom - The bottom margin.
 * @prop marginLeft - The left margin.
 * @interface
 */
type Margins = {
  marginTop?: number;
  marginRight?: number;
  marginBottom?: number;
  marginLeft?: number;
};

export default Margins;
